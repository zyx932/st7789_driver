//-----------------------------------------------
//  软件 SPI 发送一个字节 (模式0)
//-----------------------------------------------
static void SPI_WriteByte(unsigned char dat)
{
    unsigned char i;
    for (i = 0; i < 8; i++)
    {
        LCD_SCL = 0;
        if (dat & 0x80)
            LCD_SDA = 1;
        else
            LCD_SDA = 0;
        dat <<= 1;
        LCD_SCL = 1;
        _nop_(); _nop_();
    }
    LCD_SCL = 0;
}

//-----------------------------------------------
//  写命令
//-----------------------------------------------
void LCD_WriteCmd(unsigned char cmd)
{
    LCD_CS = 0;
    LCD_DC = 0;
    SPI_WriteByte(cmd);
    LCD_CS = 1;
}

//-----------------------------------------------
//  写数据
//-----------------------------------------------
void LCD_WriteData(unsigned char dat)
{
    LCD_CS = 0;
    LCD_DC = 1;
    SPI_WriteByte(dat);
    LCD_CS = 1;
}

//-----------------------------------------------
//  写16位颜色
//-----------------------------------------------
void LCD_WriteData16(unsigned int color)
{
    LCD_WriteData((unsigned char)(color >> 8));
    LCD_WriteData((unsigned char)(color & 0xFF));
}

//-----------------------------------------------
//  设置窗口
//-----------------------------------------------
void LCD_SetWindow(unsigned int xs, unsigned int ys, unsigned int xe, unsigned int ye)
{
    LCD_WriteCmd(0x2A);
    LCD_WriteData((unsigned char)(xs >> 8));
    LCD_WriteData((unsigned char)(xs & 0xFF));
    LCD_WriteData((unsigned char)(xe >> 8));
    LCD_WriteData((unsigned char)(xe & 0xFF));

    LCD_WriteCmd(0x2B);
    LCD_WriteData((unsigned char)(ys >> 8));
    LCD_WriteData((unsigned char)(ys & 0xFF));
    LCD_WriteData((unsigned char)(ye >> 8));
    LCD_WriteData((unsigned char)(ye & 0xFF));

    LCD_WriteCmd(0x2C);
}

//-----------------------------------------------
//  全屏填充
//-----------------------------------------------
void LCD_Fill(unsigned int color)
{
    unsigned int i, j;
    LCD_SetWindow(0, 0, 239, 239);
    LCD_CS = 0;
    LCD_DC = 1;
    for (i = 0; i < 240; i++)
        for (j = 0; j < 240; j++)
        {
            SPI_WriteByte((unsigned char)(color >> 8));
            SPI_WriteByte((unsigned char)(color & 0xFF));
        }
    LCD_CS = 1;
}

//-----------------------------------------------
//  矩形填充
//-----------------------------------------------
void LCD_FillRect(unsigned int xs, unsigned int ys, unsigned int xe, unsigned int ye, unsigned int color)
{
    unsigned int i, j;
    LCD_SetWindow(xs, ys, xe, ye);
    LCD_CS = 0;
    LCD_DC = 1;
    for (i = ys; i <= ye; i++)
        for (j = xs; j <= xe; j++)
        {
            SPI_WriteByte((unsigned char)(color >> 8));
            SPI_WriteByte((unsigned char)(color & 0xFF));
        }
    LCD_CS = 1;
}

//-----------------------------------------------
//  画点
//-----------------------------------------------
void LCD_DrawPixel(unsigned int x, unsigned int y, unsigned int color)
{
    if (x >= 240 || y >= 240) return;
    LCD_SetWindow(x, y, x, y);
    LCD_WriteData16(color);
}

